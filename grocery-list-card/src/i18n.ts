const TABLES: Record<string, Dict> = {
  en: EN,
  de: DE,
  es: ES,
  fr: FR,
  it: IT,
  pt: PT,
  nl: NL,
  pl: PL,
  sv: SV,
  nb: NB,
};

export type Lang = "en" | "de" | "es" | "fr" | "it" | "pt" | "nl" | "pl" | "sv" | "nb";

export function resolveLang(raw: string | undefined): Lang {
  const l = (raw ?? "").toLowerCase();
  if (l.startsWith("de")) return "de";
  if (l.startsWith("es")) return "es";
  if (l.startsWith("fr")) return "fr";
  if (l.startsWith("it")) return "it";
  if (l.startsWith("pt")) return "pt";
  if (l.startsWith("nl")) return "nl";
  if (l.startsWith("pl")) return "pl";
  if (l.startsWith("sv")) return "sv";
  if (l.startsWith("nb") || l.startsWith("nn") || l.startsWith("no")) return "nb";
  return "en";
}

export function makeT(lang: string) {
  const table = TABLES[lang] ?? EN;
  return (key: string): string => table[key] ?? EN[key] ?? key;
}
